    private void addjobpricebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addjobpricebtnActionPerformed

      
        String jobdescription = jobdescriptiontxt.getText();
        
       
        //validation
        
        
        if(jobdescriptiontxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter a service name");
        }
        
        else if((this.jobpricetxt.getText().trim().length() == 0))
        {
            JOptionPane.showMessageDialog(null, "Please enter a service price");
        }
        
        
        else
        {
        double jobprice = Double.parseDouble(jobpricetxt.getText());
        
        da.addJobPrices(jobdescription,jobprice);
        JOptionPane.showMessageDialog(null, "New Service & Price successfully added");
        jobdescriptiontxt.setText(""); 
        jobpricetxt.setText("");
        }
       
        AddJobPrices adjp=new AddJobPrices();
        adjp.setVisible(true);
        this.dispose();

    }//GEN-LAST:event_addjobpricebtnActionPerformed

