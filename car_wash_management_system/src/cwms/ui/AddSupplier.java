    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String CompanyName=CName.getText().trim();
        String SupAddress=SupAdd.getText().trim();
        String PhoneNo=SupPhone.getText().trim();
        String Email=SupEmail.getText().trim();
        if((!CompanyName.isEmpty())&&(!SupAddress.isEmpty())&&(!PhoneNo.isEmpty())&&(!Email.isEmpty()))
        {
            
            Pattern pnumPattern = Pattern.compile("(\\d{10})*");
            Matcher m = pnumPattern.matcher(PhoneNo);
            if (!m.matches()) {
                JOptionPane.showMessageDialog(null, "Invalid Phone No");
                return;
            }
            Pattern emailPattern = Pattern.compile("(^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,6}$)", Pattern.CASE_INSENSITIVE);
            m = emailPattern.matcher(Email);
            if (!m.matches()) {
                JOptionPane.showMessageDialog(null, "Invalid Email");
                return;
            }
            
                SupplierHandler Sup=new SupplierHandler();
                Sup.addSupplier(CompanyName,SupAddress,PhoneNo,Email);
              
                    JOptionPane.showMessageDialog(this,"Details Added Successfully");
               
        }
        else
        {
            
            JOptionPane.showMessageDialog(this,"All the fields must be populated","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

