    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        
        String itemId = ItemIdTxt.getText();
        String itemName = ItemNameTxt.getText();

        double qty = 0;
        double unitprice = 0;

        try {
            qty = Double.parseDouble(QtyTxt.getText());
            unitprice = Double.parseDouble(UpriceTxt.getText());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Field is Empty");
            return;
        }
        
        double total = Double.parseDouble(SubTotalTxt.getText());
      
        /***********************************************************************************/
        String itemid1 = ItemIdTxt.getText();
        String qty1 = QtyTxt.getText();
        da.removeqty(itemid1, qty1);
        /***********************************************************************************/
        
        int i;
        boolean found = false;
        
        for (i = 0; i < jTable1.getRowCount(); i++) {
            String id = jTable1.getValueAt(i, 0).toString();
            if (id.equals(itemId)) {
                found = true;
                break;
            }
        }
        
        double oldsubtotal = Double.parseDouble(TotalTxtF.getText());
        double newsubtotal = 0;

        if (found){
            
            double oldqty = Double.parseDouble(jTable1.getValueAt(i,2).toString());
            double newqty = oldqty + qty;
            double newtotal = newqty * unitprice;
            double oldtotal = Double.parseDouble(jTable1.getValueAt(i,4).toString());

            oldsubtotal -= oldtotal;
            newsubtotal = oldsubtotal + newtotal;
            
            jTable1.setValueAt(newqty,i,2);
            jTable1.setValueAt(unitprice, i,3);
            jTable1.setValueAt(newtotal,i,4);
          
        }
        else 
        {
            dtm.addRow(new Object[]{itemId, itemName, qty, unitprice, total});
                newsubtotal = oldsubtotal + total;
        }
        
        TotalTxtF.setText(""+newsubtotal);
        ItemIdTxt.setText("");
        ItemNameTxt.setText("");
        QtyTxt.setText("");
        UpriceTxt.setText("");
        SubTotalTxt.setText("");
       
    }//GEN-LAST:event_addBtnActionPerformed

