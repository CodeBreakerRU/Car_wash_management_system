    private void ItemIdTxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ItemIdTxtKeyReleased
        DataBase db=new DataBase(ConnectionType.MYSQL);
        Connection conn=db.getConnection();
        try {
            String qq = "select * from item where item_id=?";
            PreparedStatement pst = null;
            pst = conn.prepareStatement(qq);
            pst.setString(1, (String)ItemIdTxt.getText());
            pst.execute();
            
            ResultSet set= pst.executeQuery();
            while (set.next())
            {
                ItemNameTxt.setText(set.getString("item_name"));
                UpriceTxt.setText(set.getString("unit_price"));
            }
        } catch (Exception e) {
            
            e.printStackTrace();
        }
    }//GEN-LAST:event_ItemIdTxtKeyReleased

