    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String vcs = vCateSearchComBox.getSelectedItem().toString();
        String vms = vModelSearchTxt.getText().trim();
        String vbs = vBrandSearchTxt.getText().trim();
        if(vms.isEmpty())
        {
            vms="";
        }
        if(vbs.isEmpty())
        {
            vbs="";
        }
        if(vcs.isEmpty())
        {
            vcs="";
        }
       
        try {
             DataBase db = new DataBase(ConnectionType.MYSQL);
            Connection c = db.getConnection();
            String sql = ("SELECT * FROM vehicle WHERE veh_model = '"+vms+"' || veh_brand = '"+vbs+"' && veh_category='"+vcs+"'");
            PreparedStatement statement = (PreparedStatement) c.prepareStatement(sql);
            
          
            rs = statement.executeQuery();
            vSearchTbl.setModel(DbUtils.resultSetToTableModel(rs));

        }
        catch(SQLException e1) {
            System.out.println(e1);
        }
        catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

