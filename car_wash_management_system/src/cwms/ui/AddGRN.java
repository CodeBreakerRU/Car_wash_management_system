    private void nextAction()
    {   
       
        String sql = "SELECT sup_id,sup_name,grn_id,pur_date,total FROM grn WHERE (DATE_FORMAT(CURDATE(), \"%d-%m-%Y\"))=pur_date";
        try {
         prepst = dbcon.prepareStatement(sql);
         res=prepst.executeQuery();
                  
         grnTable0.setModel(DbUtils.resultSetToTableModel(res));
         tableCustom();
        
        } catch (Exception e) {
            e.printStackTrace();
        }
        
            supplierName.setSelectedItem(null);
            grnID.setText(null);
            grnDate.setCalendar(null);
            grnTotal.setText(null);
            
            dateFill();
            
            JOptionPane.showMessageDialog(null, "GRN added successfully.");
        
    }
    
