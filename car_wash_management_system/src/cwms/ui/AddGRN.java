    private void passData()
    {
        getSid();
        
        String tmp = grnID.getText().trim();
        String gidcheck = "SELECT grn_id FROM grn WHERE grn_id=?";
        
        try 
        {
            prepst = dbcon.prepareStatement(gidcheck);
            prepst.setString(1, tmp);
            ResultSet rest=prepst.executeQuery();
            
            if(rest.next()){
            JOptionPane.showMessageDialog(null, "The entered GRN ID already exist");
            }
            
            else if (supplierName.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(null, "Please select the Supplier Name");
            } 
                
            else if(grnID.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Please enter the GRN ID");
            }
        
            else if(grnDate.getDate()==null){
                JOptionPane.showMessageDialog(null, "Please select the Purchased Date");
            }
        
            else if(grnTotal.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Please enter the Total Amount");
            }
               
                       
            else
            {
            
                String sid = res.getString("sup_id");    
                String sname = String.valueOf(supplierName.getSelectedItem()).trim();
                String gid = grnID.getText().trim();
                java.util.Date grndate = grnDate.getDate();
                String total = grnTotal.getText().trim();
            
                SimpleDateFormat df = new SimpleDateFormat("dd-MM-yyyy");
                String gdate = df.format(grndate);
                
            
                GRNHandler gh=new GRNHandler();
                gh.addGRN(gid, sid, sname, gdate, total);
                   
                nextAction();
                   
            }
            
        } 
        catch (Exception e) 
        {
                e.printStackTrace();
        }
    }
    
    
